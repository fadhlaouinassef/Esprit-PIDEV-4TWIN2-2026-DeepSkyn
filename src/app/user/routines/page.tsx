const ProgressBar = ({ current, total, type }: { current: number; total: number; type: string }) => {
    const t = useTranslations("userRoutines");
    const percentage = total > 0 ? (current / total) * 100 : 0;
    const localizedType = ["morning", "night", "weekly"].includes(type)
        ? t(`tabs.${type}`)
        : type;
    return (
        <div className="relative flex items-center justify-center p-6 bg-card dark:bg-card/40 border border-border/50 rounded-3xl shadow-xl shadow-primary/5">
            <div className="relative w-24 h-24">
                <svg className="w-full h-full -rotate-90">
                    <circle
                        cx="48"
                        cy="48"
                        r="40"
                        stroke="currentColor"
                        strokeWidth="8"
                        fill="transparent"
                        className="text-muted/20"
                    />
                    <motion.circle
                        cx="48"
                        cy="48"
                        r="40"
                        stroke="currentColor"
                        strokeWidth="8"
                        fill="transparent"
                        strokeDasharray="251.2"
                        initial={{ strokeDashoffset: 251.2 }}
                        animate={{ strokeDashoffset: 251.2 - (251.2 * percentage) / 100 }}
                        transition={{ duration: 1, ease: "easeOut" }}
                        strokeLinecap="round"
                        className="text-primary"
                    />
                </svg>
                <div className="absolute inset-0 flex flex-col items-center justify-center">
                    <span className="text-xl font-black text-foreground">{current}/{total}</span>
                </div>
            </div>
            <div className="ml-6 flex-1">
                <h4 className="text-sm font-black text-muted-foreground uppercase tracking-widest mb-1">
                    {t("progress.routineType", { type: localizedType })}
                </h4>
                <p className="text-lg font-bold text-foreground leading-tight">
                    {percentage === 100 ? t("progress.sparkle") : percentage > 50 ? t("progress.halfway") : t("progress.keepGoing")}
                </p>
            </div>
        </div>
    );
};

