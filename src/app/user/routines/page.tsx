// --- Types ---
type RoutineType = "morning" | "night" | "weekly";

type ConsistencyResponse = {
    type: RoutineType;
    percentage: number;
    streak: number;
    days: Array<{ day: string; completed: boolean }>;
};

type ParsedRoutineMeta = {
    isAutoGenerated: boolean;
    idempotencyKey?: string;
    source?: string;
    skinType?: string;
    score?: string;
    durationWeeks?: string;
    warnings?: string;
    motivationText?: string;
};

const parseRoutineMeta = (envie?: string): ParsedRoutineMeta => {
    const raw = String(envie || "").trim();
    if (!raw.startsWith("AUTO_ROUTINE:")) {
        return {
            isAutoGenerated: false,
            motivationText: raw,
        };
    }

    const parts = raw.split(" | ").map((p) => p.trim()).filter(Boolean);
    const meta: ParsedRoutineMeta = {
        isAutoGenerated: true,
    };

    for (let i = 0; i < parts.length; i += 1) {
        const token = parts[i];
        if (i === 0 && token.startsWith("AUTO_ROUTINE:")) {
            meta.idempotencyKey = token.replace("AUTO_ROUTINE:", "").trim();
            continue;
        }

        if (token.startsWith("source=")) {
            meta.source = token.replace("source=", "").trim();
            continue;
        }

        if (token.startsWith("skinType=")) {
            meta.skinType = token.replace("skinType=", "").trim();
            continue;
        }

        if (token.startsWith("score=")) {
            meta.score = token.replace("score=", "").trim();
            continue;
        }

        if (token.startsWith("durationWeeks=")) {
            meta.durationWeeks = token.replace("durationWeeks=", "").trim();
            continue;
        }

        if (token.startsWith("warnings=")) {
            const warningHead = token.replace("warnings=", "").trim();
            const warningTail = parts.slice(i + 1).join(" | ").trim();
            meta.warnings = warningTail ? `${warningHead} | ${warningTail}` : warningHead;
            break;
        }
    }

    meta.motivationText = "Routine generated automatically from your latest quiz analysis.";
    return meta;
};

const cleanAutoGoalLabel = (objectif?: string, isAutoGenerated?: boolean) => {
    const raw = String(objectif || "").trim();
    if (!raw) return "Routine";
    if (!isAutoGenerated) return raw;
    return raw
        .replace(/\s-\sMorning$/i, "")
        .replace(/\s-\sEvening$/i, "")
        .replace(/\s-\sWeekly Plan$/i, "")
        .trim();
};

